def test_lookupColor_raw():
    ran = range(1, 255)
    elems = [0, 255] + [random.choice(ran) for i in range(0,30)]
    for r in elems:
        for g in elems:
            for b in elems:
                val = "#%.2x%.2x%.2x" % (r, g, b)
                assert graphics.lookupColor(val) == val

